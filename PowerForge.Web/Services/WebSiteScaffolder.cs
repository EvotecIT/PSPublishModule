/// <summary>Scaffolds a starter site with content and theme.</summary>
public static partial class WebSiteScaffolder
{
    private const string DefaultSchemaBaseUrl = "https://raw.githubusercontent.com/EvotecIT/PSPublishModule/main/Schemas/";

    private sealed class SuiteStarterProjectSeed
    {
        public string Slug { get; init; } = string.Empty;
        public string Name { get; init; } = string.Empty;
        public string Surface { get; init; } = string.Empty;
        public string SurfaceCatalogKey { get; init; } = string.Empty;
        public string QuickStartType { get; init; } = string.Empty;
        public string QuickStartTarget { get; init; } = string.Empty;
        public string RelatedContentFileName { get; init; } = string.Empty;
    }

    /// <summary>Creates a new site scaffold.</summary>
    /// <param name="outputPath">Output directory.</param>
    /// <param name="siteName">Optional site name.</param>
    /// <param name="baseUrl">Optional base URL.</param>
    /// <param name="themeEngine">Template engine identifier.</param>
    /// <param name="maintenanceProfileName">Maintenance profile (`conservative`, `balanced`, `aggressive`).</param>
    /// <param name="starterProfileName">Starter profile (`standard`, `multi-project-api-suite`).</param>
    /// <param name="suiteProjectSlug">Optional first suite project slug to materialize for the multi-project API suite starter.</param>
    /// <param name="suiteProjectName">Optional first suite project display name to materialize for the multi-project API suite starter.</param>
    /// <param name="suiteProjectSurface">Optional first suite project API surface (`powershell`, `dotnet`).</param>
    /// <returns>Result payload describing created files.</returns>
    public static WebScaffoldResult Scaffold(string outputPath, string? siteName, string? baseUrl, string? themeEngine, string? maintenanceProfileName = null, string? starterProfileName = null, string? suiteProjectSlug = null, string? suiteProjectName = null, string? suiteProjectSurface = null)
    {
        if (string.IsNullOrWhiteSpace(outputPath))
            throw new ArgumentException("Output path is required.", nameof(outputPath));

        var fullOutput = Path.GetFullPath(outputPath);
        if (Directory.Exists(fullOutput) && Directory.EnumerateFileSystemEntries(fullOutput).Any())
            throw new InvalidOperationException($"Output path is not empty: {fullOutput}");

        Directory.CreateDirectory(fullOutput);

        var name = string.IsNullOrWhiteSpace(siteName) ? "MySite" : siteName.Trim();
        var url = string.IsNullOrWhiteSpace(baseUrl) ? "https://example.com" : baseUrl.Trim();
        var engine = string.IsNullOrWhiteSpace(themeEngine) ? "simple" : themeEngine.Trim();
        var isScriban = engine.Equals("scriban", StringComparison.OrdinalIgnoreCase);
        var themeName = isScriban ? "nova" : "base";
        var maintenanceProfile = NormalizeMaintenanceProfile(maintenanceProfileName);
        var maintenanceBudgets = ResolveMaintenanceBudgets(maintenanceProfile);
        var starterProfile = NormalizeStarterProfile(starterProfileName);
        var multiProjectApiSuiteStarter = starterProfile.Equals("multi-project-api-suite", StringComparison.OrdinalIgnoreCase);
        var firstSuiteProject = ResolveSuiteStarterProjectSeed(multiProjectApiSuiteStarter, suiteProjectSlug, suiteProjectName, suiteProjectSurface);

        var created = 0;

        var contentPagesRoot = Path.Combine(fullOutput, "content", "pages");
        Directory.CreateDirectory(contentPagesRoot);
        created += WriteFile(Path.Combine(contentPagesRoot, "index.md"),
@"---
title: Welcome
description: Starter page generated by PowerForge.Web.
---

# Welcome

This site was scaffolded by PowerForge.Web.
");

        if (multiProjectApiSuiteStarter)
        {
            var suitePagesRoot = Path.Combine(contentPagesRoot, "projects");
            Directory.CreateDirectory(suitePagesRoot);
            created += WriteFile(Path.Combine(suitePagesRoot, "api-suite.md"),
@"---
title: API Suite
description: Placeholder landing page for the multi-project API suite.
---

# API Suite

This starter reserves `/projects/api-suite/` for the engine-owned API suite portal.

Next steps:

- add project entries in `data/projects/catalog.json`
- place generated API sources under `projects-sources/`
- run `powerforge-web pipeline --config .\pipeline.json --mode dev`

Once `project-apidocs` has real inputs, it will replace this placeholder route with the generated suite portal.
");
        }

        var contentDocsRoot = Path.Combine(fullOutput, "content", "docs");
        Directory.CreateDirectory(contentDocsRoot);
        created += WriteFile(Path.Combine(contentDocsRoot, "index.md"),
@"---
title: Documentation
description: Getting started guides and references.
layout: docs
meta.eyebrow: Documentation
---

