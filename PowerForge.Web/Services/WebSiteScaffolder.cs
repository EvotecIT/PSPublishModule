    private static SuiteStarterProjectSeed? ResolveSuiteStarterProjectSeed(bool multiProjectApiSuiteStarter, string? slug, string? name, string? surface)
    {
        var hasProjectInput = !string.IsNullOrWhiteSpace(slug) || !string.IsNullOrWhiteSpace(name) || !string.IsNullOrWhiteSpace(surface);
        if (!hasProjectInput)
            return null;

        if (!multiProjectApiSuiteStarter)
            throw new ArgumentException("Suite project seed options require starterProfile 'multi-project-api-suite'.", nameof(slug));

        var resolvedSurface = NormalizeSuiteStarterProjectSurface(surface);
        var resolvedSlug = NormalizeSuiteStarterProjectSlug(slug);
        var resolvedName = string.IsNullOrWhiteSpace(name) ? null : name.Trim();

        if (string.IsNullOrWhiteSpace(resolvedSlug) && string.IsNullOrWhiteSpace(resolvedName))
            throw new ArgumentException("Provide --suite-project-slug, --suite-project-name, or both when materializing the first suite project.");

        resolvedSlug ??= BuildSuiteStarterProjectSlugFromName(resolvedName!);
        resolvedName ??= BuildSuiteStarterProjectNameFromSlug(resolvedSlug);

        var identifierSuffix = BuildSuiteStarterIdentifierSuffix(resolvedName, resolvedSlug);
        return new SuiteStarterProjectSeed
        {
            Slug = resolvedSlug,
            Name = resolvedName,
            Surface = resolvedSurface,
            SurfaceCatalogKey = resolvedSurface.Equals("dotnet", StringComparison.OrdinalIgnoreCase) ? "apiDotNet" : "apiPowerShell",
            QuickStartType = resolvedSurface.Equals("dotnet", StringComparison.OrdinalIgnoreCase)
                ? $"{identifierSuffix}Client"
                : $"Invoke-{identifierSuffix}Action",
            QuickStartTarget = resolvedSurface.Equals("dotnet", StringComparison.OrdinalIgnoreCase)
                ? $"T:{identifierSuffix}Client"
                : $"ps:Invoke-{identifierSuffix}Action",
            RelatedContentFileName = $"{resolvedSlug}-api-guides.json"
        };
    }

    private static string NormalizeSuiteStarterProjectSurface(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return "powershell";

        var normalized = value.Trim().ToLowerInvariant();
        return normalized switch
        {
            "powershell" => "powershell",
            "pwsh" => "powershell",
            "ps" => "powershell",
            "dotnet" => "dotnet",
            ".net" => "dotnet",
            "csharp" => "dotnet",
            "cs" => "dotnet",
            _ => throw new ArgumentException($"Unsupported suite project surface '{value}'. Use powershell or dotnet.", nameof(value))
        };
    }

    private static string? NormalizeSuiteStarterProjectSlug(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return null;

        var trimmed = value.Trim().ToLowerInvariant();
        var buffer = new List<char>(trimmed.Length);
        var previousDash = false;
        foreach (var character in trimmed)
        {
            if (char.IsLetterOrDigit(character))
            {
                buffer.Add(character);
                previousDash = false;
                continue;
            }

            if (character is '-' or '_' or ' ' or '.')
            {
                if (!previousDash && buffer.Count > 0)
                {
                    buffer.Add('-');
                    previousDash = true;
                }
            }
        }

        var normalized = new string(buffer.ToArray()).Trim('-');
        return string.IsNullOrWhiteSpace(normalized) ? null : normalized;
    }

    private static string BuildSuiteStarterProjectSlugFromName(string value)
    {
        return NormalizeSuiteStarterProjectSlug(value)
               ?? throw new ArgumentException($"Unable to derive a suite project slug from '{value}'.", nameof(value));
    }

    private static string BuildSuiteStarterProjectNameFromSlug(string slug)
    {
        var parts = slug.Split(new[] { '-', '_' }, StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries);
        if (parts.Length == 0)
            return "Sample Project";

        return string.Join(" ", parts.Select(static part =>
        {
            if (part.Length == 1)
                return part.ToUpperInvariant();
            return char.ToUpperInvariant(part[0]) + part[1..];
        }));
    }

    private static string BuildSuiteStarterIdentifierSuffix(string name, string slug)
    {
        var source = !string.IsNullOrWhiteSpace(name) ? name : slug;
        var builder = new List<char>(source.Length);
        foreach (var character in source)
        {
            if (char.IsLetterOrDigit(character))
                builder.Add(character);
        }

        var compact = new string(builder.ToArray());
        if (string.IsNullOrWhiteSpace(compact))
            return "SampleProject";

        return compact;
    }
